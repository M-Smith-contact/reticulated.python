def drawSquare(myTurtle, sideLength):
    myTurtle.forward( sideLength)
    myTurtle.right(90) # side 1
    myTurtle.forward( sideLength)
    myTurtle.right(90) # side 2
    myTurtle.forward( sideLength)
    myTurtle.right(90) # side 3
    myTurtle.forward( sideLength)
    myTurtle.right(90) # side 4
